dealcard <- function(s, i = TRUE) {
  
  #i is TRUE if validating only for one card (hit card)
  
  upcard <- c('J','Q','K')
  value <- 0
  
  if (substr(s[1],1,1) %in% upcard) {
    value <- 10
  }else{
    value <- as.integer(substr(s[1],1,2))
  }
  if (!i) {
    if (substr(s[2],1,1) %in% upcard) {
      value <- value + 10
    }else{
      value <- value + as.integer(substr(s[2],1,2))
    }
  }
  return(value)
}

